#include "libft.h"
#include <stdlib.h>

void	ft_lstclear(t_Llist **lst, void (*del)(void*))
{
	t_Llist	*temp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		temp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = temp;
	}
	*lst = NULL;
}
